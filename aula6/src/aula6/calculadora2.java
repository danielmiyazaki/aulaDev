package aula6;

public class calculadora2 {
	public int somar(int x, int y) {
		
		int resultado = x+y;
		
		return resultado;
	}
	public int subtrair(int x, int y) {
		
		int resultado = x-y;
		
		return resultado;
	}
	public double dividir(double x,double y) {
		
		double resultado = x/y;
		
		return resultado;
	}
	public double multiplicar(double x,double y) {
		
		double resultado = x*y;
		
		return resultado;
	}

}
